package zxptsystem.helper;

public class QueryAction {
	private String QueryTypeValue;
	private String UrlReplace;
	private String url;
	public String getQueryTypeValue() {
		return QueryTypeValue;
	}
	public void setQueryTypeValue(String queryTypeValue) {
		QueryTypeValue = queryTypeValue;
	}
	public String getUrlReplace() {
		return UrlReplace;
	}
	public void setUrlReplace(String urlReplace) {
		UrlReplace = urlReplace;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
}
